export type Language = 'en' | 'ar';

type TranslationKeys = typeof translations['en'];

export function t<N extends keyof TranslationKeys, K extends keyof TranslationKeys[N]>(
  lang: Language, 
  namespace: N, 
  key: K, 
  params?: Record<string, string | number>
): string {
  // @ts-ignore
  let result = translations[lang]?.[namespace]?.[key] as string;
  if (!result) result = translations['en'][namespace][key] as string;
  
  if (params && result) {
    Object.keys(params).forEach(p => {
      result = result.replace(`{{${p}}}`, String(params[p]));
    });
  }
  return result;
}
